package org.easystogu.db.vo.table;

public class WSFConfigVO {
	public String name;
	public String value;
	public String type;
	public String desc1;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDesc1() {
		return desc1;
	}
	public void setDesc1(String desc) {
		this.desc1 = desc;
	}
}
